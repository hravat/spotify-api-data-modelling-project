CREATE TABLE FACT_AUDIOBOOK
(

    ID VARCHAR(100),
    MARKET_SR_KEY BIGINT
)


INSERT INTO PUBLIC.FACT_AUDIOBOOK 
(
ID,
MARKET_SR_KEY 
)
SELECT 
	SA.ID,
	COALESCE (DM.MARKET_SR_KEY,-1) 
	FROM PUBLIC.SPOTIFY_AUDIOBOOKS SA 
		LEFT OUTER JOIN PUBLIC.DIM_MARKET DM 
	ON SA.AVAILABLE_MARKET = DM.ALPHA_2;

 
	

